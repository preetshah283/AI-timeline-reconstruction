def perform_ml_anomaly_detection(df_unified_events_input):
    print("[*] Building ML feature matrix...")
    ml_df = df_unified_events_input.copy()

    # Encode categorical features
    event_enc = LabelEncoder()
    source_enc = LabelEncoder()
    ml_df["EventID_encoded"] = event_enc.fit_transform(ml_df["EventID"].astype(str))
    ml_df["Source_encoded"] = source_enc.fit_transform(ml_df["Source"].astype(str))

    # Time features
    ml_df["Time_numeric"] = pd.to_datetime(ml_df["Timestamp"], utc=True).astype("int64") // 10**9
    ml_df = ml_df.sort_values("Timestamp").reset_index(drop=True)

    # TimeDelta
    ml_df["TimeDelta"] = ml_df["Time_numeric"].diff().fillna(0)
    ml_df["TimeDelta"] = ml_df["TimeDelta"].clip(upper=3600)

    # ProcessName extraction
    ml_df["ProcessName"] = ml_df["Description"].apply(extract_process)
    process_enc = LabelEncoder()
    ml_df["ProcessName_encoded"] = process_enc.fit_transform(ml_df["ProcessName"])

    # Final feature matrix
    feature_cols = [
        "EventID_encoded",
        "Source_encoded",
        "ProcessName_encoded",
        "Time_numeric",
        "TimeDelta"
    ]
    X = ml_df[feature_cols]

    # Train Isolation Forest
    print("[*] Training Isolation Forest on timeline...")
    model = IsolationForest(
        contamination="auto",
        n_estimators=200,
        random_state=42,
        n_jobs=-1
    )
    ml_df["AnomalyScore_raw"] = model.fit_predict(X)
    ml_df["AnomalyScore_confidence"] = model.decision_function(X)

    # Normalize confidence to RiskScore
    scaler = MinMaxScaler(feature_range=(0, 100))
    ml_df["RiskScore"] = scaler.fit_transform(
        -ml_df["AnomalyScore_confidence"].values.reshape(-1, 1)
    ).flatten().round(1)

    anomaly_count = (ml_df["AnomalyScore_raw"] == -1).sum()
    print(f"[+] Anomaly detection complete. {anomaly_count} anomalous events detected from {len(ml_df)} total.")
    print(f"[+] Anomaly rate: {anomaly_count/len(ml_df)*100:.2f}%")

    return ml_df

def identify_attack_chains(df_unified_events_with_alerts_and_mitre, ml_full_with_risk_scores, time_gap_minutes=2):
    print("[*] Identifying attack chains...")
    # Filter for suspicious events
    threat_df = df_unified_events_with_alerts_and_mitre[df_unified_events_with_alerts_and_mitre["Alert"] != "Normal"].copy()

    # Ensure threat_df has the same index as ml_full_with_risk_scores for accurate RiskScore merging
    # It's crucial that both dataframes originated from df_unified_events and maintain their indices.
    # For this scenario, assuming filtering preserves index alignment for direct assignment.
    threat_df["RiskScore"] = ml_full_with_risk_scores["RiskScore"]

    # Build attack chains using the helper function
    threat_df = build_attack_chains(threat_df, time_gap_minutes=time_gap_minutes)

    # Calculate chain risk summary
    chain_risk = threat_df.groupby("AttackChainID").agg(
        EventCount=("Timestamp", "count"),
        AvgRiskScore=("RiskScore", "mean"),
        MaxRiskScore=("RiskScore", "max"),
        Techniques=("MITRE_ID", lambda x: list(x.dropna().unique())),
        Tactics=("MITRE_Tactic", lambda x: list(x.dropna().unique()))
    ).sort_values("EventCount", ascending=False)

    print("[+] Attack Chain Identification complete.")
    print("[+] Attack Chain Risk Summary (Top 10 by Event Count):")
    print(chain_risk.head(10).to_string())

    return threat_df, chain_risk

