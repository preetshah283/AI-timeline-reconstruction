def extract_process(desc):
    match = re.search(r'[\\/]([^\\/]+\.exe)', str(desc), re.IGNORECASE)
    if match:
        return match.group(1).lower()
    match = re.search(r'([A-Za-z0-9_\-]+\.exe)', str(desc), re.IGNORECASE)
    if match:
        return match.group(1).lower()
    return "unknown"

def hunt_threats(row):
    desc = str(row['Description']).lower()
    source = str(row['Source']).lower()
    event_id = str(row['EventID'])

    if event_id == '1102':
        return "CRITICAL: Audit Logs Cleared (Defense Evasion)"
    elif event_id == '7045':
        return "WARNING: New Service Installed (Potential Persistence)"

    suspicious_keywords = ['powershell', 'cmd.exe', '.vbs', 'empire', 'mimikatz']
    for keyword in suspicious_keywords:
        if keyword in desc:
            return f"ALERT: Suspicious Keyword ({keyword})"

    if event_id == '1' and 'sysmon' in source:
         return "INFO: Process Created"

    return "Normal"

def map_to_mitre(row):
    desc = str(row["Description"]).lower()
    event_id = str(row["EventID"])

    if event_id in MITRE_MAPPINGS:
        tid, name, tactic = MITRE_MAPPINGS[event_id]
        return pd.Series([tid, name, tactic])

    for keyword, (tid, name, tactic) in MITRE_MAPPINGS.items():
        if keyword in desc:
            return pd.Series([tid, name, tactic])

    return pd.Series(["—", "Unclassified Activity", "Unknown"])

def build_attack_chains(df, time_gap_minutes=2):
    df = df.sort_values("Timestamp").copy()

    chain_id = 0
    chains = []
    prev_time = None

    for _, row in df.iterrows():
        curr_time = row["Timestamp"]

        if prev_time is None or (curr_time - prev_time) > pd.Timedelta(minutes=time_gap_minutes):
            chain_id += 1

        chains.append(chain_id)
        prev_time = curr_time

    df["AttackChainID"] = chains
    return df

