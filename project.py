def evtx_to_json_stream(evtx_file, output_file):
    with Evtx(evtx_file) as log:
        with open(output_file, "w", encoding="utf-8") as f:
            for record in log.records():
                f.write(json.dumps(record.xml()) + "\n")

def parse_security_event_xml(xml_string):
    event_data = {}
    try:
        root = ET.fromstring(xml_string)
        namespaces = {
            'win': 'http://schemas.microsoft.com/win/2004/08/events/event'
        }
        event_id_element = root.find('.//win:EventID', namespaces=namespaces)
        if event_id_element is not None:
            event_data['EventID'] = event_id_element.text
        time_created_element = root.find('.//win:TimeCreated', namespaces=namespaces)
        if time_created_element is not None:
            event_data['EventTime'] = time_created_element.get('SystemTime')
        provider_name_element = root.find('.//win:Provider', namespaces=namespaces)
        if provider_name_element is not None:
            event_data['ProviderName'] = provider_name_element.get('Name')
        event_data_element = root.find('.//win:EventData', namespaces=namespaces)
        if event_data_element is not None:
            for data_element in event_data_element.findall('win:Data', namespaces=namespaces):
                name = data_element.get('Name')
                value = data_element.text
                if name and value:
                    event_data[name] = value
        user_data_element = root.find('.//win:UserData', namespaces=namespaces)
        if user_data_element is not None:
            for child in user_data_element:
                if child.tag:
                    tag_name = child.tag.split('}')[-1]
                    event_data[tag_name] = child.text
    except ET.ParseError:
        pass
    return event_data

